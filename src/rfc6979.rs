//! RFC 6979 deterministic nonce (HMAC-SHA256), matching libsecp256k1 `hash_impl.h`
//! `secp256k1_rfc6979_hmac_sha256_*` and `nonce_function_rfc6979_impl` key layout
//! (`seckey32 || msg_reduced32` for default ECDSA sign).

use sha2::{Digest, Sha256};

const BLOCK: usize = 64;

fn hmac_sha256(key: &[u8], data: &[u8]) -> [u8; 32] {
    let mut padded = [0u8; BLOCK];
    if key.len() > BLOCK {
        padded[..32].copy_from_slice(&Sha256::digest(key));
    } else {
        padded[..key.len()].copy_from_slice(key);
    }
    let mut inner_key = padded;
    for b in inner_key.iter_mut() {
        *b ^= 0x36;
    }
    let inner = Sha256::new()
        .chain_update(inner_key)
        .chain_update(data)
        .finalize();
    let mut outer_key = padded;
    for b in outer_key.iter_mut() {
        *b ^= 0x5c;
    }
    Sha256::new()
        .chain_update(outer_key)
        .chain_update(inner)
        .finalize()
        .into()
}

/// Same layout as `secp256k1_rfc6979_hmac_sha256` in libsecp256k1.
pub(crate) struct Rfc6979HmacSha256 {
    v: [u8; 32],
    k: [u8; 32],
    retry: bool,
}

impl Rfc6979HmacSha256 {
    pub(crate) fn initialize(key: &[u8]) -> Self {
        let mut rng = Self {
            v: [0u8; 32],
            k: [0u8; 32],
            retry: false,
        };
        rng.v.fill(0x01);
        rng.k.fill(0x00);

        let mut buf = Vec::with_capacity(33 + key.len());
        buf.extend_from_slice(&rng.v);
        buf.push(0x00);
        buf.extend_from_slice(key);
        rng.k = hmac_sha256(&rng.k, &buf);

        rng.v = hmac_sha256(&rng.k, &rng.v);

        buf.clear();
        buf.extend_from_slice(&rng.v);
        buf.push(0x01);
        buf.extend_from_slice(key);
        rng.k = hmac_sha256(&rng.k, &buf);

        rng.v = hmac_sha256(&rng.k, &rng.v);
        rng.retry = false;
        rng
    }

    pub(crate) fn generate(&mut self, out: &mut [u8]) {
        if self.retry {
            let mut buf = Vec::with_capacity(33);
            buf.extend_from_slice(&self.v);
            buf.push(0x00);
            self.k = hmac_sha256(&self.k, &buf);
            self.v = hmac_sha256(&self.k, &self.v);
        }

        let mut offset = 0usize;
        while offset < out.len() {
            self.v = hmac_sha256(&self.k, &self.v);
            let n = (out.len() - offset).min(32);
            out[offset..offset + n].copy_from_slice(&self.v[..n]);
            offset += n;
        }
        self.retry = true;
    }
}

/// One RFC6979 candidate: re-initialize from `keydata`, then call `generate` `counter + 1` times,
/// keeping the last 32-byte output — matches `nonce_function_rfc6979_impl` in libsecp256k1.
pub(crate) fn nonce32_rfc6979_libsecp(keydata: &[u8], counter: u32) -> [u8; 32] {
    let mut rng = Rfc6979HmacSha256::initialize(keydata);
    let mut out = [0u8; 32];
    for _ in 0..=counter {
        rng.generate(&mut out);
    }
    out
}

